% --- Executes on button press in LoadImage.
function LoadImage_Callback(hObject, eventdata, handles)
% hObject    handle to LoadImage (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
[filename,pathname] = uigetfile('*.bmp','Select lesion image');
I = imread(fullfile(pathname, filename));
handles.I = I;
guidata(hObject, handles);
handles;
set(handles.File,'String',pathname);
axes(handles.axes1);
imshow(I);
title('Imagen Dermatoscópica');


% --- Executes on button press in LBP.
function LBP_Callback(hObject, eventdata, handles)
% hObject    handle to LBP (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
[Image_Pre, Image_Seg, Image_lbp, DataLBP, Class] = TaskHMI(handles.I);
handles.Image_Pre = Image_Pre;
handles.Image_Seg = Image_Seg;
handles.Image_lbp = Image_lbp;
axes(handles.axes2);
imshow(Image_Pre)
title('Imagen Procesada');
axes(handles.axes5);
axis off;
imhist(Image_Pre)
title('Histograma')
axes(handles.axes3);
imshow(Image_Seg)
title('Área de la Lesion');
axes(handles.axes4);
imshow(uint8(Image_lbp), []);
title('Imagen LBP');
axes(handles.axes6);
axis off;
bar(DataLBP);
title('Histograma');
handles.DataLBP = DataLBP;
set(handles.uitable3,'data',DataLBP);

